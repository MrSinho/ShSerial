uint16_t fggSerialOpen(const char* port, const uint16_t baud_rate, const uint32_t n_bits_x_call, const uint32_t max_byte_interval, const uint32_t max_timeout, const FggSerialFlags flags, FggSerialHandle* p_handle) {
#ifdef _WIN32
	char _port[10] = "\\\\.\\";
	strcat(_port, port);
	p_handle->_handle = CreateFile(_port, flags, 0, NULL, OPEN_EXISTING, FILE_ATTRIBUTE_NORMAL, NULL);
	if (p_handle->_handle == INVALID_HANDLE_VALUE) {
#ifndef NDEBUG
		printf("FggSerial error, handle %p: cannot open serial port %s\n", p_handle, port);
#endif // NDEBUG
		return 0;
	}

	DCB dcb = { 0 };
	dcb.DCBlength = sizeof(DCB);

	uint16_t result = fggSerialCheckResult(p_handle, GetCommState(p_handle->_handle, &dcb), "cannot set comm state");
	if (!result) { return 0; }

	dcb.BaudRate = baud_rate;
	dcb.ByteSize = n_bits_x_call;
	dcb.StopBits = ONESTOPBIT;
	dcb.Parity = NOPARITY;
	
	result = fggSerialCheckResult(p_handle, SetCommState(p_handle->_handle, &dcb), "cannot set communication state");
	if (!result) { return 0; }

	COMMTIMEOUTS timeout = { 0 };
	timeout.ReadIntervalTimeout = max_byte_interval;		
	timeout.ReadTotalTimeoutMultiplier = 1;			
	timeout.ReadTotalTimeoutConstant = max_timeout;	
	timeout.WriteTotalTimeoutConstant = max_timeout;
    timeout.WriteTotalTimeoutMultiplier = 1;

	result = fggSerialCheckResult(p_handle, SetCommTimeouts(p_handle->_handle, &timeout), "cannot set timeouts");
	if (!result) { return 0; }

	return 1;

#endif // _WIN32


#ifdef __linux__
	uint8_t _flags = 0;
	switch(flags) {
	  case FGG_SERIAL_READ_BIT: _flags = O_RDONLY; break;
	  case FGG_SERIAL_WRITE_BIT: _flags = O_WRONLY; break;
	  case FGG_SERIAL_READ_BIT | FGG_SERIAL_WRITE_BIT: _flags = O_RDWR; break;
  	}
  	handle.descriptor = open("/dev/ttyUSB0", (int)_flags);
	if (!handle.descriptor) {
#ifndef NDEBUG
		printf("FggSerial error: cannot open serial port %s\n", port);
#endif // NDEBUG
		return;
	}

#endif // __linux__
}

uint16_t fggSerialClose(FggSerialHandle* p_handle) {
#ifdef _WIN32
	int result = fggSerialCheckResult(p_handle, CloseHandle(p_handle->_handle), "cannot close serial port");
	if (!result) { return 0; }
	return 1;
#endif // _WIN32
}

uint16_t fggSerialReadBuffer(const uint32_t size, void* dst, unsigned long* bytes_read, FggSerialHandle* p_handle) {
#ifdef _WIN32
	int result = fggSerialCheckResult(p_handle, ReadFile(p_handle->_handle, dst, size, bytes_read, NULL), "cannot read from serial port");
	if (!result) { return 0; } 
	return 1;
#endif // _WIN32
}

uint16_t fggSerialSetReceiveMask(FggSerialCommMask mask, FggSerialHandle* p_handle) {
#ifdef _WIN32
	int result = fggSerialCheckResult(p_handle, SetCommMask(p_handle->_handle, mask), "cannot set receive mask");
	if (!result) { return 0; } 

  	DWORD event_mask = 0;
  	result = fggSerialCheckResult(p_handle, WaitCommEvent(p_handle->_handle, &event_mask, NULL), "cannot set waiting comm event");
	if (!result) { return 0; }
	
	return 1;
#endif // _WIN32
}

uint16_t fggSerialWriteBuffer(const uint32_t size, const void* src, FggSerialHandle* p_handle) {
#ifdef _WIN32
	unsigned long bytes_written = 0;
	int result = fggSerialCheckResult(p_handle, WriteFile(p_handle->_handle, src, size, &bytes_written, NULL), "cannot write to serial port");
	if (!result) { return 0; }
	return 1;
#endif // _WIN32
}


uint16_t fggSerialCheckResult(FggSerialHandle* p_handle, const int result, const char* msg) {
	if (!result) {
#ifndef NDEBUG
		printf("FggSerial error, handle 0x%p: %s, %s\n", p_handle, msg, fggSerialTranslateError());
#endif // NDEBUG
		return 0;		
	}
	return 1;
}

